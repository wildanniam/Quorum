import assert from "node:assert/strict";
import { execFileSync, spawnSync } from "node:child_process";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";

const projectRoot = process.cwd();
const verifierSource = fs.readFileSync(
  path.join(projectRoot, "scripts", "browser-qa-provenance.mjs"),
  "utf8",
);

function git(cwd, args) {
  return execFileSync("git", args, {
    cwd,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  }).trim();
}

function writeJson(filePath, value) {
  fs.writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`);
}

function createFixture() {
  const cwd = fs.mkdtempSync(
    path.join(os.tmpdir(), "quorum-browser-provenance-"),
  );

  for (const directory of ["docs", "scripts", "src"]) {
    fs.mkdirSync(path.join(cwd, directory), { recursive: true });
  }

  fs.writeFileSync(
    path.join(cwd, "scripts", "browser-qa-provenance.mjs"),
    verifierSource,
  );
  fs.writeFileSync(
    path.join(cwd, "scripts", "browser-qa.mjs"),
    "console.log('browser qa');\n",
  );
  fs.writeFileSync(path.join(cwd, "src", "app.js"), "export const app = 1;\n");
  writeJson(path.join(cwd, "package.json"), {
    name: "browser-provenance-fixture",
    private: true,
    scripts: { test: "node --test" },
    dependencies: { react: "19.2.4" },
    devDependencies: { playwright: "1.60.0" },
    engines: { node: ">=22.0.0" },
  });
  writeJson(path.join(cwd, "package-lock.json"), {
    name: "browser-provenance-fixture",
    lockfileVersion: 3,
    packages: {},
  });

  git(cwd, ["init"]);
  git(cwd, ["config", "user.email", "browser-qa@quorum.local"]);
  git(cwd, ["config", "user.name", "Quorum Browser QA"]);
  git(cwd, ["add", "."]);
  git(cwd, ["commit", "-m", "test: add browser qa source"]);

  fs.writeFileSync(
    path.join(cwd, "docs", "BROWSER_QA.md"),
    "# Browser QA\n\nThis file is generated by `npm run browser:qa`.\n",
  );
  fs.writeFileSync(
    path.join(cwd, "docs", "DEMO_EVIDENCE.md"),
    "# Demo Evidence\n",
  );
  git(cwd, ["add", "docs"]);
  git(cwd, ["commit", "-m", "docs: record browser qa"]);

  return cwd;
}

function runVerifier(cwd) {
  const result = spawnSync(
    process.execPath,
    ["scripts/browser-qa-provenance.mjs"],
    { cwd, encoding: "utf8" },
  );
  const rawReport = result.stdout.trim() || result.stderr.trim();

  return {
    report: JSON.parse(rawReport),
    status: result.status,
  };
}

function withFixture(assertion) {
  const cwd = createFixture();

  try {
    assertion(cwd);
  } finally {
    fs.rmSync(cwd, { force: true, recursive: true });
  }
}

withFixture((cwd) => {
  const packageJsonPath = path.join(cwd, "package.json");
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
  packageJson.scripts.provenance = "node scripts/browser-qa-provenance.mjs";
  writeJson(packageJsonPath, packageJson);
  git(cwd, ["add", "package.json"]);
  git(cwd, ["commit", "-m", "test: add evidence command"]);

  const result = runVerifier(cwd);
  assert.equal(result.status, 0);
  assert.equal(result.report.ok, true);
  assert.deepEqual(result.report.changedInputFiles, []);
});

withFixture((cwd) => {
  fs.writeFileSync(path.join(cwd, "src", "app.js"), "export const app = 2;\n");
  const result = runVerifier(cwd);

  assert.notEqual(result.status, 0);
  assert.match(result.report.error, /uncommitted changes/);
});

withFixture((cwd) => {
  fs.writeFileSync(path.join(cwd, "src", "app.js"), "export const app = 2;\n");
  git(cwd, ["add", "src/app.js"]);
  git(cwd, ["commit", "-m", "feat: change browser input"]);
  const result = runVerifier(cwd);

  assert.notEqual(result.status, 0);
  assert.equal(result.report.ok, false);
  assert.deepEqual(result.report.changedInputFiles, ["src/app.js"]);
});

withFixture((cwd) => {
  const packageJsonPath = path.join(cwd, "package.json");
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
  packageJson.dependencies.next = "16.2.8";
  writeJson(packageJsonPath, packageJson);
  git(cwd, ["add", "package.json"]);
  git(cwd, ["commit", "-m", "build: change runtime dependency"]);
  const result = runVerifier(cwd);

  assert.notEqual(result.status, 0);
  assert.equal(result.report.ok, false);
  assert.notEqual(
    result.report.sourceInputFingerprint,
    result.report.currentInputFingerprint,
  );
});

withFixture((cwd) => {
  fs.appendFileSync(
    path.join(cwd, "docs", "BROWSER_QA.md"),
    "\nManual mutation.\n",
  );
  const result = runVerifier(cwd);

  assert.notEqual(result.status, 0);
  assert.equal(result.report.browserDocumentMatches, false);
});

withFixture((cwd) => {
  fs.appendFileSync(path.join(cwd, "docs", "BROWSER_QA.md"), "\nNew run.\n");
  fs.writeFileSync(path.join(cwd, "src", "app.js"), "export const app = 2;\n");
  git(cwd, ["add", "docs/BROWSER_QA.md", "src/app.js"]);
  git(cwd, ["commit", "-m", "test: mix source and browser evidence"]);
  const result = runVerifier(cwd);

  assert.notEqual(result.status, 0);
  assert.match(result.report.error, /also changes source files/);
});

